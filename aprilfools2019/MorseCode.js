module.exports = {  
    encode : function (string) {
        var stringMorse = '';
      
        for(var i = 0; i < string.length; i++){
            var c = string.charAt(i).toLowerCase();
            var cMorse = MorseCode[c];
            if(cMorse){
                stringMorse += cMorse;
            }
            stringMorse += ' ';
        }
        return stringMorse;
    },
    decode : function (stringMorse) {
        var string = '';
        var cleanStringMorse = stringMorse.replace(/_/g,'-');
        var tokensMorse = cleanStringMorse.split(/ +/);
      
        for(var i = 0; i < tokensMorse.length; i++){
            var cMorse = tokensMorse[i];
            var c = ReverseMorseCode[cMorse];
            if(c){
                string += c;
            }
        }
      
        return string;
    }
}